int getMemo(int row, int col, vector<vector<int>>& memo)
{
	if (row < 0 || col < 0)
	{
		return 0;
	}

	return memo[row][col];
}
int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid)
{

	int m = obstacleGrid.size();
	int n = obstacleGrid[0].size();

	vector<int>temp(n, 0);
	vector<vector<int>>memo(m, temp);

	for (int i = 0; i < m; i++)
	{
		for (int j = 0; j < n; j++)
		{
			if (obstacleGrid[i][j] != 1)
			{
				if (i == 0 && j == 0)
				{
					memo[i][j] = 1;
				}
				else
				{
					memo[i][j] = getMemo(i - 1, j, memo) + getMemo(i, j - 1, memo);
				}
			}
		}
	}

	return memo[m - 1][n - 1];
}





